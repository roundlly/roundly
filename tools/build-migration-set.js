/**
 * Phase 4 — build a consolidated, ordered migration set from the LIVE function
 * dump (pg_get_functiondef) + the live policy snapshot. Every function body is
 * taken byte-for-byte from the live DB, so the repo set reflects reality, not
 * the drifted hand-applied .sql files.
 *
 * Inputs  (CSV exports from the Supabase SQL editor):
 *   tools/live-functions.csv   (signature,definition)  — 64 functions
 *   tools/live-policies.csv    (tablename,policyname,cmd,qual,with_check)
 * Output:
 *   db/migrations/*.sql      ordered, documented
 *
 * Run: node tools/build-migration-set.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const FN_CSV  = path.join(__dirname, 'live-functions.csv');
const POL_CSV = path.join(__dirname, 'live-policies.csv');
const OUT_DIR = path.join(ROOT, 'db', 'migrations');

// --- minimal RFC-4180 CSV parser (handles quoted fields w/ embedded "" + newlines) ---
function parseCsv(text) {
  const rows = [];
  let row = [], field = '', i = 0, inQ = false;
  while (i < text.length) {
    const c = text[i];
    if (inQ) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i += 2; continue; }
        inQ = false; i++; continue;
      }
      field += c; i++; continue;
    }
    if (c === '"') { inQ = true; i++; continue; }
    if (c === ',') { row.push(field); field = ''; i++; continue; }
    if (c === '\r') { i++; continue; }
    if (c === '\n') { row.push(field); rows.push(row); row = []; field = ''; i++; continue; }
    field += c; i++;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

const fnRows = parseCsv(fs.readFileSync(FN_CSV, 'utf8'));
const header = fnRows.shift(); // signature,definition
const fns = fnRows
  .filter(r => r.length >= 2 && r[0].trim())
  .map(r => ({ sig: r[0].trim(), def: r[1].replace(/\s+$/, '') }));

// name (without args) -> list of {sig, def}
const byName = new Map();
for (const f of fns) {
  const name = f.sig.replace(/\(.*$/, '');
  if (!byName.has(name)) byName.set(name, []);
  byName.get(name).push(f);
}

// Pull a function's definition(s) by bare name. dropExtra filters overloads.
function defsFor(name, predicate) {
  const list = byName.get(name) || [];
  const chosen = predicate ? list.filter(predicate) : list;
  if (!chosen.length) throw new Error('missing function in dump: ' + name);
  return chosen.map(f => f.def.trim() + ';').join('\n\n');
}
function maybe(name) { return byName.has(name) ? defsFor(name) : null; }

const HDR = (title, body) =>
`-- ============================================================================
-- ${title}
-- ----------------------------------------------------------------------------
-- Source of truth: LIVE Supabase DB (pg_get_functiondef dump, 2026-06-23).
-- Generated by tools/build-migration-set.js — do not hand-edit; regenerate.
-- Apply in filename order on a fresh database. Idempotent (CREATE OR REPLACE).
-- ============================================================================

${body}
`;

fs.mkdirSync(OUT_DIR, { recursive: true });

// ---- 03 helpers (no game logic; referenced by RPCs + policies) ----
const HELPER_NAMES = [
  'huddle_is_claimant', 'huddle_assert_room_table', 'huddle_assert_host',
  'huddle_phase_start_ms', 'huddle_shuffle_jsonb', 'huddle_mafia_assert_narrator',
  'is_admin', 'touch_updated_at', 'feedback_posts_touch',
];
const helpers = HELPER_NAMES.map(maybe).filter(Boolean).join('\n\n');

// ---- 04 universal room RPCs ----
const universal = ['huddle_create_room', 'huddle_claim_seat', 'huddle_leave_seat',
  'huddle_close_room', 'huddle_migrate_seat'].map(maybe).filter(Boolean).join('\n\n');

// ---- 05/06/07 per-game (hot, cham, liar) ----
const pick = (prefix, exclude = []) =>
  [...byName.keys()].filter(n => n.startsWith(prefix) && !exclude.includes(n) && !HELPER_NAMES.includes(n)).sort()
    .map(maybe).filter(Boolean).join('\n\n');

const hot  = pick('huddle_hot_');
const cham = pick('huddle_cham_');
const liar = pick('huddle_liar_');

// ---- 08 mafia — canonical start_game ONLY (drop the stray 1-arg overload).
// Exclude HELPER_NAMES so huddle_mafia_assert_narrator isn't emitted twice. ----
const mafiaNames = [...byName.keys()].filter(n => n.startsWith('huddle_mafia_') && !HELPER_NAMES.includes(n)).sort();
let mafia = '';
for (const n of mafiaNames) {
  if (n === 'huddle_mafia_start_game') {
    // keep ONLY the 5-arg canonical overload; document the dropped 1-arg.
    const five = (byName.get(n) || []).find(f => /,/.test(f.sig));
    mafia += `-- huddle_mafia_start_game: canonical 5-arg form ONLY. The stray 1-arg\n`;
    mafia += `-- overload (huddle_mafia_start_game(text)) is intentionally OMITTED here and\n`;
    mafia += `-- dropped by 09_drop_legacy.sql to resolve the live PGRST203 ambiguity.\n`;
    mafia += five.def.trim() + ';\n\n';
  } else {
    mafia += defsFor(n) + '\n\n';
  }
}

// ---- 09 admin + feedback analytics ----
const admin = ['admin_stats'].map(maybe).filter(Boolean).join('\n\n');

fs.writeFileSync(path.join(OUT_DIR, '02_helpers.sql'), HDR('02 — Helper functions (claimant/host/table asserts, time, shuffle, is_admin, triggers)', helpers));
fs.writeFileSync(path.join(OUT_DIR, '03_universal_room_rpcs.sql'), HDR('03 — Universal room RPCs (create / claim / leave / close / migrate seat)', universal));
fs.writeFileSync(path.join(OUT_DIR, '04_hotseat_rpcs.sql'), HDR('04 — Hot Seat RPCs', hot));
fs.writeFileSync(path.join(OUT_DIR, '05_chameleon_rpcs.sql'), HDR('05 — Chameleon RPCs', cham));
fs.writeFileSync(path.join(OUT_DIR, '06_liar_rpcs.sql'), HDR('06 — Liar\'s Cup RPCs', liar));
fs.writeFileSync(path.join(OUT_DIR, '07_mafia_rpcs.sql'), HDR('07 — Mafia RPCs (canonical start_game; classic night-engine RPCs retained as live)', mafia));
fs.writeFileSync(path.join(OUT_DIR, '08_admin_analytics.sql'), HDR('08 — Admin analytics', admin));

// summary
const all = [...byName.keys()].sort();
console.log('Functions in dump:', fns.length, 'distinct names:', byName.size);
console.log('start_game overloads kept: 1 (5-arg), dropped: 1 (1-arg)');
console.log('Wrote db/migrations/03..09');
